const GROUPS: { label: string; data: Section[] }[] = [
  { label: "شروع و مفاهیم", data: CONCEPTS },
  { label: "پنل مدیریت", data: PANEL },
  { label: "ربات مدیر", data: ADMIN_BOT },
  { label: "ربات نمایندگان", data: RESELLER_BOT },
];

export default function Help() {
  const [tab, setTab] = useState(0);
  const [q, setQ] = useState("");
  const ql = q.trim().toLowerCase();

  // When searching, look across ALL groups (not just the active tab) so nothing is missed.
  const filterSec = (s: Section): Section | null => {
    if (!ql) return s;
    if (s.title.toLowerCase().includes(ql)) return s;
    const items = s.items.filter((i) => i.t.toLowerCase().includes(ql) || i.d.toLowerCase().includes(ql));
    return items.length ? { ...s, items } : null;
  };
  const source = ql ? GROUPS.flatMap((g) => g.data) : GROUPS[tab].data;
  const sections = source.map(filterSec).filter(Boolean) as Section[];

  return (
    <Box>
      <Card sx={{ mb: 2, bgcolor: (t) => alpha(t.palette.primary.main, t.palette.mode === "dark" ? 0.18 : 0.06), border: "none" }}>
        <CardContent>
          <Stack direction="row" spacing={1.5} alignItems="center">
            <HelpOutlineIcon color="primary" sx={{ fontSize: 36 }} />
            <Box>
              <Typography variant="h6" sx={{ fontWeight: 800 }}>راهنمای کامل سامانه</Typography>
              <Typography variant="body2" color="text.secondary">
                همهٔ امکاناتِ پنلِ مدیریت، رباتِ مدیر و رباتِ نمایندگان — دسته‌بندی‌شده و قابلِ جستجو.
              </Typography>
            </Box>
          </Stack>
        </CardContent>
      </Card>

      <Stack direction={{ xs: "column", sm: "row" }} spacing={2} sx={{ mb: 2 }} alignItems="center">
        <Tabs value={tab} onChange={(_, v) => setTab(v)} variant="scrollable" scrollButtons="auto"
          sx={{ flexGrow: 1 }} aria-disabled={!!ql}>
          {GROUPS.map((g) => <Tab key={g.label} label={g.label} />)}
        </Tabs>
        <TextField size="small" placeholder="جستجو در راهنما…" value={q} sx={{ minWidth: { xs: "100%", sm: 220 } }}
          onChange={(e) => setQ(e.target.value)}
          InputProps={{ startAdornment: <SearchIcon fontSize="small" sx={{ ml: 1, color: "text.secondary" }} /> }} />
      </Stack>

      {ql && (
        <Typography variant="caption" color="text.secondary" sx={{ display: "block", mb: 1.5 }}>
          نتایجِ جستجو در همهٔ بخش‌ها — برای بازگشت به دسته‌بندی، جستجو را خالی کنید.
        </Typography>
      )}

      <Divider sx={{ mb: 2 }} />

      {sections.length === 0 ? (
        <Typography color="text.secondary" sx={{ p: 3, textAlign: "center" }}>موردی یافت نشد.</Typography>
      ) : (
        sections.map((s) => <SectionCard key={s.id} s={s} />)
      )}
    </Box>
  );
}
